#include "libft.h"

int	ft_countdigits(int n)
{
	int	i;

	i = 1;
	if (n == -2147483648)
		n++;
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n > 9)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
